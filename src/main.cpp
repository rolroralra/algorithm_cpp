using namespace std;


int T, N, K;

int main() {
    freopen("../src/sample_input.txt", "r", stdin);
    scanf("%d\n", &T);

    for (int test_case = 1; test_case <= T; test_case++) {
        scanf("%d %d\n", &N, &K);

        int result = 0;

        printf("#%d %d\n", test_case, result);
    }

    return 0;
}
